    static int countPairs(List<Integer> numbers2, int k) {
        List<Integer> numbers = new ArrayList<>(new HashSet<>(numbers2)).stream().sorted().collect(Collectors.toList());
        Collections.sort(numbers);

        System.out.println("total: " + numbers.size());
        Map<Integer, Integer> pairMap = new HashMap<>();
        Set<Integer> set = new HashSet<>();
        int count = 0;

        for (int i = 0; i < numbers.size(); i++) {
            Integer a = numbers.get(i);
            Integer b = a + k;

            if (set.add(b)) {
                if (set.contains(a))
                    count++;
            }
        }

        return count;
    }

    static int countPairs2(List<Integer> numbers, int k) {
        Map<Integer, Boolean> pairMap = new HashMap<>();
        int count = 0;

        for (int i = 0; i < numbers.size(); i++) {
            for (int j = 0; j < numbers.size(); j++) {
                if (i != j) {
                    int a = numbers.get(i);
                    int b = numbers.get(j);

                    if (a + k == b) {
                        pairMap.put(b, true);
                        //System.out.println(a + ", " + b  + ", " + k);
                        count++;
                    }
                }
            }
        }

        //System.out.println(pairMap);
        return pairMap.size();
    }

